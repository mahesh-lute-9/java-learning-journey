

class BitwiseDemo{

	public static void main(String[] args){
	
		int x = 7;

		int y = 8;

		System.out.println("x & y = " + (x & y));
		
		System.out.println("x | y = " + (x | y));
		
		System.out.println("x ^ y = " + (x ^ y));
		
		System.out.println("~x = " + (~x));
		
		System.out.println("x << 2 = " + (x << 2));
		
		System.out.println("x >> 2 = " + (x >> 2));
	}
}
