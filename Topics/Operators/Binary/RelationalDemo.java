

class RelationalDemo{

	public static void main(String[] args){
	
		int x = 8;

		int y = 7;

		System.out.println(x<y);
		
		System.out.println(x>y);

		System.out.println(x<=y);

		System.out.println(x>=y);

		System.out.println(x==y);

		System.out.println(x!=y);

	//In relational Operators it compares two values and return a boolean value based on the condition as (true or false).
	}
}
