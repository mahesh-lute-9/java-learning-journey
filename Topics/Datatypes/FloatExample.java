
//Floating point Datatypes



class FloatExample{

	public static void main(String[] args){
		
		//storing daily running distance in kilometers

		float distance = 5.75f;

		System.out.println("Distance Run Today: " + distance + " km");
	}
}


/* Output:
 * 	Distance Run Today: 5.75 km
 *
 *----------------------------------------------------------------------------
 *	
 *	Memory:	The float is of 4 bytes, which means 32 bits.
 *
 *	Range: Approximately +- 3.4*10^38(about 6-7 decimal digits precision)
 *
 *	Used for: Storing decimal numbers with moderate precision.
 *		
 *
 * IMP: For float values we use f or F at the end of the values, if we don't give then the value 
 * 	gets consider as double.
 * */
