
//Working with Strings


class Demo{

	public static void main(String[] args){
		
		String[] names = {"Mahesh","Amol","Kunal"};

		for (String name : names){
		
			System.out.println("Hello " + name);
		}
	}
}


/* Output:
 * 	Hello Mahesh
	Hello Amol
	Hello Kunal
 *
 *
 * 	Scenario:
 * 		Displaying studnet names, employee names, products, etc
 * */
