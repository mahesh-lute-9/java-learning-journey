

class Demo{

	public static void main(String[] args){
	
		for(int i = 1; i <= 10; i++){
			
			if(i % 2 != 0){
				
				System.out.println(i + " is odd");
			}
		}
	}
}


/* Output:
 * 	1 is odd
	3 is odd
	5 is odd
	7 is odd
	9 is odd
 *
 *
 * Explanation:
 * 	To print the odd numbers between the range we are having for-loop and if statement. This will print the odd numbers.
 * */
